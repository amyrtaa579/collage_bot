def get_initial_scores() -> Dict[str, int]:
    """Возвращает словарь с начальными нулевыми баллами для всех специальностей"""
    return {code: 0 for code in SPECIALTIES}

def filter_scores_by_gender(scores: Dict[str, int], gender: str) -> Dict[str, int]:
    """
    Для девушек (female) обнуляем баллы по специальностям с тяжёлым физическим трудом.
    Для остальных возвращаем исходные баллы.
    """
    if gender == "female":
        filtered = scores.copy()
        for code in EXCLUDED_FOR_FEMALE:
            filtered[code] = 0
        return filtered
    return scores

def get_top_specialties(scores: Dict[str, int]) -> List[Tuple[str, str, int]]:
    """
    Возвращает список топ-2 специальностей в виде кортежей (код, название, баллы).
    Всегда возвращает 2 специальности (даже с нулевыми баллами).
    """
    # Сортируем по убыванию баллов
    sorted_items = sorted(scores.items(), key=lambda x: x[1], reverse=True)
    
    # Берём первые два элемента
    top_two = sorted_items[:2]
    
    result = []
    for code, points in top_two:
        result.append((code, SPECIALTIES[code], points))
    
    return result